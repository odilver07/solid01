package SolidBookRefactor;

public class Page {
	 private Integer number;
	    private String content;

	    public Page(Integer number, String content) {
	        this.number = number;
	        this.content = content;
	    }

	    public Integer getNumber() {
	        return number;
	    }

	    public String getContent() {
	        return content;
	    }
}
